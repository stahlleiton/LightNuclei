void makeHistograms()
{
  for (const auto& col : data_ALICE) {
    // Create output file
    const auto fileName = "hData_"+col.first+".root";
    TFile file(fileName.c_str(), "RECREATE");
    if (!file.IsOpen() || file.IsZombie()) throw std::runtime_error("[ERROR] Failed to create file: "+fileName);
    // Load data
    for (const auto& chg : col.second) {
      for (const auto& par : chg.second) {
	for (const auto& obs : par.second) {
	  // Get binning
	  std::set<float> xS({-100.}), yS({-100.}), zS({-100}); // add lower boundary
	  zS.insert(obs.second.rbegin()->first.begin()->second.second);
	  for (const auto& r : obs.second) {
	    zS.insert(r.first.begin()->second.first);
	    yS.insert(r.second.rbegin()->first.begin()->second.second);
	    for (const auto& c : r.second) {
	      yS.insert(c.first.begin()->second.first);
	      xS.insert(c.second.rbegin()->first.begin()->second.second);
	      for (const auto& p : c.second) {
		xS.insert(p.first.begin()->second.first);
	      }
	    }
	  }
	  std::vector<float> x(xS.begin(), xS.end()), y(yS.begin(), yS.end()), z(zS.begin(), zS.end()); // add bins
	  x.push_back(100.); y.push_back(100.); z.push_back(100.); // add upper boundary
	  const size_t nBinX(x.size()-1), nBinY(y.size()-1), nBinZ(z.size()-1);
	  // Create histogram
	  const auto name = "hData_"+chg.first+par.first+"_"+obs.first;
	  TH3F hist(name.c_str(), "", nBinX, x.data(), nBinY, y.data(), nBinZ, z.data());
	  hist.GetXaxis()->SetTitle(obs.second.begin()->second.begin()->second.begin()->first.begin()->first.c_str());
	  hist.GetYaxis()->SetTitle(obs.second.begin()->second.begin()->first.begin()->first.c_str());
	  hist.GetZaxis()->SetTitle(obs.second.begin()->first.begin()->first.c_str());
	  // Fill histogram
	  for (const auto& r : obs.second) {
	    for (size_t iz=0; iz<nBinZ; iz++) {
	      if (z[iz+1]<r.first.begin()->second.first || (iz>0 && z[iz+1]==r.first.begin()->second.first)) continue;
	      if (z[iz]>r.first.begin()->second.second || (iz<nBinZ-1 && z[iz]==r.first.begin()->second.second)) break;
	      for (const auto& c : r.second) {
		for (size_t iy=0; iy<nBinY; iy++) {
		  if (y[iy+1]<c.first.begin()->second.first || (iy>0 && y[iy+1]==c.first.begin()->second.first)) continue;
		  if (y[iy]>c.first.begin()->second.second || (iy<nBinY-1 && y[iy]==c.first.begin()->second.second)) break;
		  for (const auto& p : c.second) {
		    for (size_t ix=0; ix<nBinX; ix++) {
		      if (x[ix+1]<p.first.begin()->second.first || (ix>0 && x[ix+1]==p.first.begin()->second.first)) continue;
		      if (x[ix]>p.first.begin()->second.second || (ix<nBinX-1 && x[ix]==p.first.begin()->second.second)) break;
		      hist.SetBinContent(ix+1, iy+1, iz+1, p.second[0]);
		      const auto fc1 = p.second[2]<=0. ? 0. : p.second[0];
		      const auto fc2 = p.second[4]<=0. ? 0. : p.second[0];
		      const auto yErrLo = sumError(p.second[2] - fc1, p.second[4] - fc2);
		      const auto yErrHi = sumError(p.second[1] - fc1, p.second[3] - fc2);
		      const auto err = std::max(yErrLo, yErrHi);
		      hist.SetBinError(ix+1, iy+1, iz+1, err);
		    }
		  }
		}
	      }
	    }
	  }
	  hist.Write(name.c_str());
	}
      }
    }
    file.Write();
    file.Close();
  }
};


